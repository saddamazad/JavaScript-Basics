// when a variable(any data type) is set to `NULL`, it's removed from the memory and doesn't exist anymore
// when a variable(any data type) is set to undefined, the variable is stored in the memory with undefined value


// null and undefined have same value(nothing)

console.log( null == undefined ); // true


// null and undefined are not same type

console.log( null === undefined ); // false


console.log( typeof null ); // object

console.log( typeof undefined ); // undefined
